# Creating a dictionary
person = {
    "name": "Alice",
    "age": 30,
    "city": "New York"
} 

# Accessing values
print(person["name"])  # Output: Alice

# Adding a new key-value pair
person["job"] = "Engineer"

# Updating a value
person["age"] = 31

# Removing a key-value pair
del person["city"]

# Printing the updated dictionary
print(person)
